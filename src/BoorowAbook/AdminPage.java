    private void Student_table(){
        int i;
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost:3306/bookissue","root","");
            pst=con.prepareStatement("SELECT * FROM student");
            
            ResultSet rs=pst.executeQuery();
            ResultSetMetaData rsm=rs.getMetaData();
            i=rsm.getColumnCount();
            
            DefaultTableModel Df=(DefaultTableModel)txtstdDB.getModel();
            Df.setRowCount(0);
            
            while(rs.next()){
                   Vector v=new Vector();
                   for(int a=1; a<=i; a++){
                       v.add(rs.getString("matricule"));
                       v.add(rs.getString("std_id"));
                       v.add(rs.getString("name"));
                       v.add(rs.getString("faculty"));
                       v.add(rs.getString("department"));
                       v.add(rs.getString("Std_level"));
                       v.add(rs.getString("contact"));
                       v.add(rs.getString("gender"));
                   }
                   Df.addRow(v);
               }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"error here: "+e.getMessage());
        }
    }
    
    private void Books_table(){
        int j;
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost:3306/bookissue","root","");
            pst=con.prepareStatement("SELECT book_id,std_id,title,author,status,due_date,return_date,issue FROM books");
        
            ResultSet rs=pst.executeQuery();
            ResultSetMetaData rsm=rs.getMetaData();
            j=rsm.getColumnCount();
            
            DefaultTableModel Def=(DefaultTableModel)txtbookDB.getModel();
            Def.setRowCount(0);
            
            while(rs.next()){
                   Vector v=new Vector();
                   for(int a=1; a<=j; a++){
                       v.add(rs.getString("book_id"));
                       v.add(rs.getString("std_id"));
                       v.add(rs.getString("title"));
                       v.add(rs.getString("author"));
                       v.add(rs.getString("status"));
                       v.add(rs.getString("due_date"));
                       v.add(rs.getString("return_date"));
                       v.add(rs.getString("issue"));
                   }
                   Def.addRow(v);
               }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"error here: "+e.getMessage());
        }
        
    }
