    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
       String id=txtid.getText();
       String title=txttitle.getText();
       String author=txtauthor.getText();
       String availability=txtstatus.getSelectedItem().toString();
       int issue=0;
       try{
           Class.forName("com.mysql.cj.jdbc.Driver");
           con=DriverManager.getConnection("jdbc:mysql://localhost:3306/bookissue","root","");
           pst=con.prepareStatement("INSERT INTO books(book_id, title, author, status, issue)VALUES(?,?,?,?,?)");
           //Statement st=con.createStatement();
           pst.setString(1, id);
           pst.setString(2, title);
           pst.setString(3, author);
           pst.setString(4, availability);
           pst.setInt(5, issue);
           
           pst.executeUpdate();
           
           JOptionPane.showMessageDialog(null, "Record added");
           Books_table();
           
           txtid.setText("");
           txttitle.setText("");
           txtauthor.setText("");
       }catch(Exception e){
           JOptionPane.showMessageDialog(null,"error here: "+e.getMessage());
       }
        Books_table();
    }//GEN-LAST:event_jButton3ActionPerformed

